import {
  WinCrossParseDiagnosticsSchema,
  WinCrossPreferenceProfileSchema,
  WinCrossRawJobModelSchema,
  type WinCrossParseDiagnostics,
  type WinCrossPreferenceProfile,
  type WinCrossRawJobModel,
} from '@/lib/exportData/types';

interface ParsedSections {
  order: string[];
  sections: Record<string, string[]>;
}

export interface ParseWinCrossPreferenceResult {
  raw: WinCrossRawJobModel;
  profile: WinCrossPreferenceProfile;
  diagnostics: WinCrossParseDiagnostics;
}

export function parseWinCrossPreferenceJob(input: string | Buffer): ParseWinCrossPreferenceResult {
  const raw = parseRawWinCrossJob(input);
  const diagnostics = buildParseDiagnostics(raw);
  const profile = buildPreferenceProfile(raw);

  return { raw, profile, diagnostics };
}

export function parseRawWinCrossJob(input: string | Buffer): WinCrossRawJobModel {
  const decoded = decodeWinCrossInput(input);
  const parsed = splitSections(decoded.text);

  const versionLines = parsed.sections.VERSION ?? [];
  const preferences = parsed.sections.PREFERENCES ?? [];
  const sigFooter = parsed.sections.SIGFOOTER ?? [];
  const banners = parsed.sections.BANNERS ?? [];
  const tables = parsed.sections.TABLES ?? [];

  const tablePatternHints = {
    tableCount: tables.filter((line) => /^T\d+\^\d+/i.test(line)).length,
    useCount: tables.filter((line) => /^USE\s*=\s*/i.test(line)).length,
    afCount: tables.filter((line) => /^AF\s*=\s*/i.test(line)).length,
    sbaseCount: tables.filter((line) => /^SBase\s*:/i.test(line)).length,
  };

  return WinCrossRawJobModelSchema.parse({
    encoding: decoded.encoding,
    sectionOrder: parsed.order,
    rawSections: parsed.sections,
    versionLines,
    preferenceSection: classifyPreferenceSection(preferences),
    sigFooterLines: sigFooter,
    bannerSection: classifyBannerSection(banners),
    tableSection: classifyTableSection(tables),
    titleLines: parsed.sections.TITLE ?? [],
    tablePatternHints,
  });
}


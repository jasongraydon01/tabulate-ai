/**
 * Validate filter expressions against actual .sav data.
 *
 * For split-based filters, only flags as zero-base when ALL splits for a given
 * ruleId+questionId produce zero respondents (a single zero split may be legitimate).
 *
 * If R fails entirely, returns all counts as -1 (does not block the pipeline).
 */
export async function validateFilterBases(
  filters: TableFilter[],
  spssPath: string,
  outputDir: string,
): Promise<ZeroBaseValidationResult> {
  console.warn('[DEPRECATED] validateFilterBases() called — this should not be invoked in the active pipeline. Use DeterministicBaseEngine instead.');
  const startTime = Date.now();

  // Collect all expressions
  const entries = collectFilterExpressions(filters);

  if (entries.length === 0) {
    return { counts: [], zeroBaseFilters: [], durationMs: Date.now() - startTime };
  }

  // Sanitize expressions — skip any that fail
  const safeEntries: ExpressionEntry[] = [];
  const unsafeEntries: FilterBaseCount[] = [];

  for (const entry of entries) {
    const result = sanitizeRExpression(entry.expression);
    if (result.safe) {
      safeEntries.push(entry);
    } else {
      console.warn(
        `[ZeroBaseValidator] Skipping unsafe expression for ${entry.filterId}: ${result.error}`
      );
      unsafeEntries.push({
        filterId: entry.filterId,
        questionId: entry.questionId,
        ruleId: entry.ruleId,
        expression: entry.expression,
        respondentCount: -1,
        totalN: 0,
        isZeroBase: false,
      });
    }
  }

  if (safeEntries.length === 0) {
    return {
      counts: unsafeEntries,
      zeroBaseFilters: [],
      durationMs: Date.now() - startTime,
    };
  }

  // Generate and execute R script
  const rScript = generateValidationScript(spssPath, safeEntries);
  const scriptPath = path.join(outputDir, 'zero_base_validation.R');
  writeFileSync(scriptPath, rScript);

  let rOutput: RValidationOutput;
  try {
    rOutput = await executeRScript(scriptPath);
  } catch (err) {
    // R failed entirely — return all as -1, don't block pipeline
    const errorMsg = err instanceof Error ? err.message : String(err);
    console.warn(`[ZeroBaseValidator] R execution failed: ${errorMsg}. Returning -1 for all filters.`);

    const fallbackCounts: FilterBaseCount[] = safeEntries.map(entry => ({
      filterId: entry.filterId,
      questionId: entry.questionId,
      ruleId: entry.ruleId,
      expression: entry.expression,
      respondentCount: -1,
      totalN: 0,
      isZeroBase: false,
    }));

    return {
      counts: [...fallbackCounts, ...unsafeEntries],
      zeroBaseFilters: [],
      durationMs: Date.now() - startTime,
    };
  }

  // Map R results back to FilterBaseCount
  const counts: FilterBaseCount[] = [];
  for (let i = 0; i < safeEntries.length; i++) {
    const entry = safeEntries[i];
    const rResult = rOutput.results[i];
    const respondentCount = rResult?.respondentCount ?? -1;
    const totalN = rResult?.totalN ?? rOutput.totalN;

    counts.push({
      filterId: entry.filterId,
      questionId: entry.questionId,
      ruleId: entry.ruleId,
      expression: entry.expression,
      respondentCount,
      totalN,
      isZeroBase: respondentCount === 0,
    });
  }

  // Add back unsafe entries
  counts.push(...unsafeEntries);

  // Determine zero-base filters
  // For split-based filters: only flag when ALL splits for a ruleId+questionId are zero
  const zeroBaseFilters: FilterBaseCount[] = [];

  // Group by ruleId+questionId to handle split logic
  const groupedBySplitKey = new Map<string, { splits: FilterBaseCount[]; primary: FilterBaseCount | null }>();

  for (const count of counts) {
    const entry = entries.find(e => e.filterId === count.filterId);
    const groupKey = `${count.ruleId}::${count.questionId}`;

    if (!groupedBySplitKey.has(groupKey)) {
      groupedBySplitKey.set(groupKey, { splits: [], primary: null });
    }

    const group = groupedBySplitKey.get(groupKey)!;
    if (entry?.isSplit) {
      group.splits.push(count);
    } else {
      group.primary = count;
    }
  }

  for (const [, group] of groupedBySplitKey) {
    // Primary (non-split) filters: flag if zero-base
    if (group.primary && group.primary.isZeroBase) {
      zeroBaseFilters.push(group.primary);
    }

    // Split filters: flag only when ALL splits are zero (or errored)
    if (group.splits.length > 0) {
      const allZeroOrError = group.splits.every(s => s.respondentCount === 0 || s.respondentCount === -1);
      const hasAtLeastOneZero = group.splits.some(s => s.respondentCount === 0);
      if (allZeroOrError && hasAtLeastOneZero) {
        // Add all zero-base splits
        for (const split of group.splits) {
          if (split.isZeroBase) {
            zeroBaseFilters.push(split);
          }
        }
      }
    }
  }

  const durationMs = Date.now() - startTime;
  console.log(
    `[ZeroBaseValidator] Validated ${counts.length} expressions in ${durationMs}ms. ` +
    `Zero-base: ${zeroBaseFilters.length}`
  );

  return { counts, zeroBaseFilters, durationMs };
}
